    private void jButBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButBuscarActionPerformed
        int opcion = jCBBuscar.getSelectedIndex();
        String campo = "";
        String campoAux = "";
        switch (seleccionEntidad()) {
            case "Pelicula":
                switch (opcion) {
                    case 0:
                        campo = jTFTitulo.getText();
                        break;
                    case 1:
                        campo = jTFPelDirectorNombre.getText();
                        campoAux = jTFPelDirectorApellido.getText();
                        break;
                    case 2:
                        campo = jTFPelAnio.getText();
                        break;
                }
                break;
            case "Serie":
                switch (opcion) {
                    case 0:
                        campo = jTFTitulo.getText();
                        break;
                    case 1:
                        campo = jTFSerTemporadas.getText();
                        break;
                    case 2:
                        campo = jTFSerEpisodios.getText();
                        break;
                }
                break;
            case "Director":
                switch (opcion) {
                    case 0:
                        campo = jTFDirNombre.getText();
                        break;
                    case 1:
                        campo = jTFDirApellido.getText();
                        break;
                    case 2:
                        campo = jTFDirNacionalidad.getText();
                        break;
                    case 3:
                        campo = jTFDirPelicula.getText();
                        break;
                }
                break;
            case "Usuario":
                switch (opcion) {
                    case 0:
                        campo = jTFUsuAlias.getText();
                        break;
                    case 1:
                        campo = jTFUsuNombre.getText();
                        break;
                    case 2:
                        campo = jTFUsuApellido.getText();
                        break;
                    case 3:
                        campo = jTFUsuEmail.getText();
                        break;
                    case 4:
                        campo = jFTFCelular.getText();
                        break;
                }
                break;
        }
        jTAEstado.setText(campo + campoAux + " buscado");
        vaciarCampos();
    }//GEN-LAST:event_jButBuscarActionPerformed

