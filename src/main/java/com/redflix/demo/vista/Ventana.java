    private void jButAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButAgregarActionPerformed
        String campo = "";
        String campoAux = "";
        switch (seleccionEntidad()) {
            case "Pelicula":
                String titulo = jTFTitulo.getText();
                campo = titulo;
                String directorNombre = jTFPelDirectorNombre.getText();
                String directorApellido = jTFPelDirectorApellido.getText();
                int anio = Integer.parseInt(jTFPelAnio.getText());
                String resumen = jTAPelResumen.getText();
                Pelicula peliculaNueva = new Pelicula();
                Director directorNuevo = new Director();
                peliculaNueva.setTitulo(titulo);
                peliculaNueva.setAnio(anio);
                peliculaNueva.setResumen(resumen);
                directorNuevo.setNombre(directorNombre);
                directorNuevo.setApellido(directorApellido);
                peliculaNueva.setDirector(directorNuevo);
                break;

            case "Serie":
                titulo = jTFTitulo.getText();
                campo = titulo;
                int numTemporadas = Integer.parseInt(jTFSerTemporadas.getText());
                int numEpisodios = Integer.parseInt(jTFSerEpisodios.getText());
                Serie serieNueva = new Serie();

                serieNueva.setTitulo(titulo);
                serieNueva.setNumeroEpisodios(numEpisodios);
                serieNueva.setNumeroTemporadas(numTemporadas);
                break;
            case "Director":
                String nombre = jTFDirNombre.getText();
                campo = nombre;
                String apellido = jTFDirApellido.getText();
                campoAux = apellido;

                String nacionalidad = jTFDirNacionalidad.getText();
                String pelicula = jTFDirPelicula.getText();
                directorNuevo = new Director();

                directorNuevo.setNombre(nombre);
                directorNuevo.setApellido(apellido);
                directorNuevo.setNacionalidad(nacionalidad);
                peliculaNueva = new Pelicula();

                peliculaNueva.setTitulo(pelicula);
                directorNuevo.setPelicula(peliculaNueva);
                break;
            case "Usuario":
                String alias = jTFUsuAlias.getText();
                campo = alias;
                nombre = jTFUsuNombre.getText();
                apellido = jTFUsuApellido.getText();
                String email = jTFUsuEmail.getText();
                String celular = jFTFCelular.getText();
                String contrasena = jPFUsuContrasenia.getText();
                String fecahNac = jFTFUsuFecha.getText();

                if (alias.isEmpty() || nombre.isEmpty() || apellido.isEmpty()) {
                    campo = "Por favor rellenar los campos obligatorios.";
                } else {
                    Usuario usuarioNuevo = new Usuario();
                    usuarioNuevo.setIdAlias(alias);
                    usuarioNuevo.setNombre(nombre);
                    usuarioNuevo.setApellido(apellido);
                    usuarioNuevo.setEmail(email);
                    usuarioNuevo.setCelular(celular);
                    usuarioNuevo.setContrasena(contrasena);
                    usuarioNuevo.setFechaNaimiento(fecahNac);
                }
                break;
        }
        jTAEstado.setText(campo + campoAux + " eliminado");
        vaciarCampos();

    }//GEN-LAST:event_jButAgregarActionPerformed
    private String seleccionEntidad() {
        String entidad = "";
        if (tab.equals(jPanContenido.getName())) {
            if (jRBPelicula.isSelected()) {
                entidad = "Pelicula";
            } else {
                entidad = "Serie";
            }
        } else if (tab.equals(jPanDirector.getName())) {
            entidad = "Director";
        } else if (tab.equals(jPanUsuario.getName())) {
            entidad = "Usuario";
        }
        return entidad;
    }

    private void vaciarCampos() {
        Component[] paneles = null;
        switch (seleccionEntidad()) {
            case "Pelicula":
                paneles = jPanPelicula.getComponents();
                jTFTitulo.setText("");
                break;
            case "Serie":
                paneles = jPanSerie.getComponents();
                jTFTitulo.setText("");
                break;
            case "Director":
                paneles = jPanDirector.getComponents();
                break;
            case "Usuario":
                paneles = jPanUsuario.getComponents();
                break;
        }
        for (Component c : paneles) {
            if (c instanceof JTextField) {
                ((JTextField) c).setText("");
            } else if (c instanceof JScrollPane) {
                Component C = ((JScrollPane) c).getComponent(0);
                ((JTextArea) C).setText("");
            }
        }
    }

