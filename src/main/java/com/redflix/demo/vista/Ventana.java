    private void jTabEntidadesStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jTabEntidadesStateChanged
        tab = jTabEntidades.getSelectedComponent().getName();
        switch (tab) {
            case "Contenidos":
                activarPanelPelicula();
                setPanelEnabled(jPanPelicula, true);
                setPanelEnabled(jPanSerie, false);
                break;
            case "Directores":
                activarPanelDirector();
                break;
            case "Usuarios":
                activarPanelUsuario();
        }
    }//GEN-LAST:event_jTabEntidadesStateChanged

